//Understanding Asynchronous & Synchronous

//Synchronous Transmission System
// Large amount of data can be send
// data transfers as a block
// One operation at a time
// Multi-Operation not allowed
// Loading...Loading...finish first...then move to next one
// Ex: General file upload system

//Asynchronous Transmission System
//transmission is slow
// data transfer as bit by bit / character by character
// No Loading...can work on multiprocess...can't have to wait for other works to finish
// Ex: YouTube video upload is a Asynchronous process

// When to use which one ?

// Simple process er jonne - Synchronous Ex: general / small file uploading
// Complex process er jonne - Asynchronous Ex: youtube video upload, gmail file attachment